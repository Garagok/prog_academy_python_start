# Write a Python program to check if the value a is less than 20 or not
a = int(input('Enter some number: '))

# a less than 20
if a < 20:
    print('Value of your number is less than 20')
# a equals 20
elif a == 20:
    print('Value of your number equals 20')
# a is bigger than 20
else:
    print('Value of your number is bigger than 20')
